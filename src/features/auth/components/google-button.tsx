/**
 * One-click Google sign-in. OAuth needs a full-page redirect to Google, so this
 * lets next-auth handle navigation (no `redirect: false`) and lands the user
 * back on `callbackUrl`.
 */
export function GoogleButton({ callbackUrl = "/" }: { callbackUrl?: string }) {
  const [loading, setLoading] = useState(false);

  return (
    <button
      type="button"
      disabled={loading}
      onClick={() => {
        setLoading(true);
        signIn("google", { callbackUrl });
      }}
      className="flex h-10 items-center justify-center gap-2.5 rounded-md border border-zinc-300 bg-white text-sm font-medium text-zinc-700 transition-colors hover:bg-zinc-50 disabled:cursor-not-allowed disabled:opacity-50 dark:border-zinc-700 dark:bg-zinc-900 dark:text-zinc-100 dark:hover:bg-zinc-800"
    >
      <GoogleIcon />
      {loading ? "Redirecting..." : "Continue with Google"}
    </button>
  );
}
